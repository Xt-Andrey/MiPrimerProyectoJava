public class Comparacion {
    public Comparacion() {
    }
 
    public static void main(String[] var0) {
       byte var1 = 10;
       byte var2 = 20;
       System.out.println("numero1 es igual a numero2? " + (var1 == var2));
       System.out.println("numero1 es diferente de numero2? " + (var1 != var2));
       System.out.println("numero1 es mayor que numero2? " + (var1 > var2));
       System.out.println("numero1 es menor que numero2? " + (var1 < var2));
       System.out.println("numero1 es mayor o igual que numero2? " + (var1 >= var2));
       System.out.println("numero1 es menor o igual que numero2? " + (var1 <= var2));
    }
 }